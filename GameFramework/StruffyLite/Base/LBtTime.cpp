////////////////////////////////////////////////////////////////////////////////
// BtTime.cpp

#include "BtTime.h"
#include <stdio.h>
#include <stdlib.h>
#include <time.h>

#ifdef WIN32
#include "BtTimeDX11.h"
#else
#include "BtTimeGLES.h"
#endif

////////////////////////////////////////////////////////////////////////////////
// Statics

BtFloat BtTime::m_tick = 1.0f / 60.0f;

////////////////////////////////////////////////////////////////////////////////
// GetTime

BtU32 BtTime::GetTime()
{	
	clock_t currentTime;
	currentTime = clock();

	return (BtU32)currentTime;
}

////////////////////////////////////////////////////////////////////////////////
// GetTick

BtFloat BtTime::GetTick()
{	
	return m_tick;
}

////////////////////////////////////////////////////////////////////////////////
// SetTick

void BtTime::SetTick( BtFloat tick )
{	
	m_tick = tick;
}

#ifdef WIN32

////////////////////////////////////////////////////////////////////////////////
// GetElapsedTimeInSeconds

BtFloat BtTime::GetElapsedTimeInSeconds()
{	
	return BtTimeDX11::GetElapsedTimeInSeconds();
}

////////////////////////////////////////////////////////////////////////////////
// GetDay

BtU32 BtTime::GetDay()
{	
	return BtTimeDX11::Day();
}

////////////////////////////////////////////////////////////////////////////////
// GetMonth

BtU32 BtTime::GetMonth()
{	
	return BtTimeDX11::Month();
}

////////////////////////////////////////////////////////////////////////////////
// GetYear

BtU32 BtTime::GetYear()
{	
	return BtTimeDX11::Year();
}

////////////////////////////////////////////////////////////////////////////////
// GetElapsedTimeInMilliseconds

BtFloat BtTime::GetElapsedTimeInMilliseconds()
{	
	return BtTimeDX11::GetElapsedTimeInSeconds() * 1000.0f;
}

#else

////////////////////////////////////////////////////////////////////////////////
// GetElapsedTimeInSeconds

BtFloat BtTime::GetElapsedTimeInSeconds()
{	
	return BtTimeGLES::GetElapsedTimeInSeconds();
}

////////////////////////////////////////////////////////////////////////////////
// GetDay

BtU32 BtTime::GetDay()
{	
	return BtTimeGLES::Day();
}

////////////////////////////////////////////////////////////////////////////////
// GetMonth

BtU32 BtTime::GetMonth()
{	
	return BtTimeGLES::Month();
}

////////////////////////////////////////////////////////////////////////////////
// GetYear

BtU32 BtTime::GetYear()
{	
	return BtTimeGLES::Year();
}

////////////////////////////////////////////////////////////////////////////////
// GetElapsedTimeInMilliseconds

BtFloat BtTime::GetElapsedTimeInMilliseconds()
{	
	return BtTimeGLES::GetElapsedTimeInSeconds() * 1000.0f;
}

#endif

