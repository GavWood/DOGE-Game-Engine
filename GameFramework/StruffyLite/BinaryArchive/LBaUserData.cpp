// Includes
#include "BaResource.h"
#include "BaUserData.h"

////////////////////////////////////////////////////////////////////////////////
// FixPointers

void BaUserData::FixPointers( BtU8 *pFileData, BaArchive *pArchive )
{
	m_pFileData = pFileData + sizeof(BtU32);
	m_nSize = *((BtU32*)pFileData);
}

////////////////////////////////////////////////////////////////////////////////
// Size

BtU32 BaUserData::GetSize()
{
	return m_nSize;
}

////////////////////////////////////////////////////////////////////////////////
// pData

BtU8* BaUserData::GetData()
{
	return m_pFileData;
}
