////////////////////////////////////////////////////////////////////////////////
/// SgRigidBodyImpl.cpp

#include "SgRigidBodyImpl.h"

////////////////////////////////////////////////////////////////////////////////
// Constructor

SgRigidBodyImpl::SgRigidBodyImpl()
{
}

////////////////////////////////////////////////////////////////////////////////
// FixPointers

void SgRigidBodyImpl::FixPointers( BtU8* pMemory )
{
	m_pRigidBodyFileData = (BaSgRigidBodyFileData*) pMemory;
}

////////////////////////////////////////////////////////////////////////////////
// CenterOfMass

MtVector3 SgRigidBodyImpl::CenterOfMass()
{
	return m_pRigidBodyFileData->m_v3CenterOfMass;
}

////////////////////////////////////////////////////////////////////////////////
// GetMass

BtFloat SgRigidBodyImpl::GetMass()
{
	return m_pRigidBodyFileData->m_mass;
}

////////////////////////////////////////////////////////////////////////////////
// IsDynamic

BtBool SgRigidBodyImpl::IsDynamic()
{
	if( m_pRigidBodyFileData->m_isDynamic != 0 )
	{
		return BtTrue;
	}
	else
	{
		return BtFalse;
	}
}

////////////////////////////////////////////////////////////////////////////////
// SetMass

void SgRigidBodyImpl::SetMass( BtFloat mass )
{
	m_pRigidBodyFileData->m_mass = mass;
}

////////////////////////////////////////////////////////////////////////////////
// IsDynamic

BtFloat	SgRigidBodyImpl::Restitution()
{
	return m_pRigidBodyFileData->m_restitution;
}

////////////////////////////////////////////////////////////////////////////////
// IsDynamic

BtFloat SgRigidBodyImpl::Friction()
{
	return m_pRigidBodyFileData->m_friction;
}

