////////////////////////////////////////////////////////////////////////////////
// ShAchievement

// Includes
#include "ShAchievement.h"
#include "RsUtil.h"

////////////////////////////////////////////////////////////////////////////////
// statics

// Public functions
BtQueue<ShAchievementAction, 128> ShAchievement::m_actions;

////////////////////////////////////////////////////////////////////////////////
// PushAction

//static
void ShAchievement::PushAction( ShAchievementAction action )
{
    m_actions.Push( action );
}

////////////////////////////////////////////////////////////////////////////////
// GetNumItems

//static
BtU32 ShAchievement::GetNumItems()
{
    return m_actions.GetItemCount();
}

////////////////////////////////////////////////////////////////////////////////
// PopAction

//static
ShAchievementAction ShAchievement::PopAction()
{
    ShAchievementAction action;
    action = m_actions.Pop();
    return action;
}

////////////////////////////////////////////////////////////////////////////////
// PeekAction

//static
ShAchievementAction ShAchievement::PeekAction()
{
	ShAchievementAction action;
	action = m_actions.Peek();
	return action;
}

