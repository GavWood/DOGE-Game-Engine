////////////////////////////////////////////////////////////////////////////////
// ShRecorder

// Includes
#include "ShRecorder.h"

////////////////////////////////////////////////////////////////////////////////
// statics

//static

// Public functions
BtQueue<ShRecorderAction, 128> ShRecorder::m_actions;

////////////////////////////////////////////////////////////////////////////////
// PushAction

//static
void ShRecorder::PushAction( ShRecorderAction cameraAction )
{
    m_actions.Push( cameraAction );
}

////////////////////////////////////////////////////////////////////////////////
// GetNumItems

//static
BtU32 ShRecorder::GetNumItems()
{
    return m_actions.GetItemCount();
}

////////////////////////////////////////////////////////////////////////////////
// PopAction

//static
ShRecorderAction ShRecorder::PopAction()
{
    ShRecorderAction action;
    action = m_actions.Pop();
    return action;
}

