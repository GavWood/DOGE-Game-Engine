////////////////////////////////////////////////////////////////////////////////
// FixPointers

void SgBoneGLES::FixPointers( BtU8* pMemory )
{
	// Set the file data
	m_pFileData = (BaSgBoneFileData*) pMemory;
}

////////////////////////////////////////////////////////////////////////////////
// GetInverseBindPose

const MtMatrix4& SgBoneGLES::GetInverseBindPose() const
{
	return m_pFileData->m_m4InverseBindPose;
}
